    private void HAPUSActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_HAPUSActionPerformed
        int selectedRow = TABEL.getSelectedRow();

        if (selectedRow >= 0) { // Memastikan ada baris yang dipilih
            // Menghapus data dari model tabel berdasarkan baris yang dipilih
            dataMHS.Hapus(selectedRow);

            // Memperbarui tampilan tabel
            viewTable();
            JOptionPane.showMessageDialog(this, "Data berhasil dihapus.");
        } else {
            JOptionPane.showMessageDialog(this, "Pilih baris yang ingin dihapus terlebih dahulu.", "Error", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_HAPUSActionPerformed

    private void HITUNGActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_HITUNGActionPerformed
         HITUNG.addActionListener(new ActionListener() {
        @Override
        public void actionPerformed(ActionEvent e) {
            try {
                // Mengambil input nilai 1 sampai 5 dari text field masing-masing
                double NILAI1 = Double.parseDouble(NILAI1FIELD.getText());
                double NILAI2 = Double.parseDouble(NILAI2FIELD.getText());
                double NILAI3 = Double.parseDouble(NILAI3FIELD.getText());
                double NILAI4 = Double.parseDouble(NILAI4FIELD.getText());
                double NILAI5 = Double.parseDouble(NILAI5FIELD.getText());

                // Buat objek Mahasiswa dengan nilai input dan langsung menghitung NilaiAkhir
                MAHASISWA MHS = new MAHASISWA("12345", "Nama Mahasiswa", "Alamat", "Mata Kuliah", 
                                                    NILAI1, NILAI2, NILAI3, NILAI4, NILAI5);

                // Mengambil hasil NilaiAkhir dari objek Mahasiswa
                double hasilAkhir = MHS.getNILAIAKHIR();

                // Menampilkan hasil perhitungan di txtNilaiAkhir
                NILAIAKHIRFIELD.setText(String.valueOf(hasilAkhir));

            } catch (NumberFormatException ex) {
                JOptionPane.showMessageDialog(null, "Masukkan nilai valid pada semua kolom input nilai 1 sampai 5", "Input Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    });
    }//GEN-LAST:event_HITUNGActionPerformed

    private void SIMPANActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SIMPANActionPerformed
        String NIM = NIMFIELD.getText();
        String Nama = NAMAFIELD.getText();
        String Alamat = ALAMATFIELD.getText();
        String MataKuliah = MKFIELD.getText();

        try {
            double NILAI1 = Double.parseDouble(NILAI1FIELD.getText());
            double NILAI2 = Double.parseDouble(NILAI2FIELD.getText());
            double NILAI3 = Double.parseDouble(NILAI3FIELD.getText());
            double NILAI4 = Double.parseDouble(NILAI4FIELD.getText());
            double NILAI5 = Double.parseDouble(NILAI5FIELD.getText());

            dataMHS.Input(NIM, Nama, Alamat, MataKuliah, NILAI1, NILAI2, NILAI3, NILAI4, NILAI5);
            viewTable(); // Refresh tabel
            ClearTextField(); // Kosongkan field input
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Input nilai harus berupa angka.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_SIMPANActionPerformed

