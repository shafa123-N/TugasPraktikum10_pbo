    public final void viewTable() {
    String[] namakolom = {"NIM", "Nama", "Alamat", "Mata Kuliah", "Nilai Akhir"}; 
    Object[][] objekmahasiswa = new Object[dataMHS.getAll().size()][5]; // Ubah ukuran array menjadi 5 kolom
    int i = 0; 
    for (MAHASISWA MHS: dataMHS.getAll()) {  
        Object[] arrayMahasiswa = {  
            MHS.getNIM(), 
            MHS.getNama(),  
            MHS.getAlamat(),
            MHS.getMK(), // Ambil nama mata kuliah
            MHS.getNILAIAKHIR() // Ambil nilai akhir
        }; 
        objekmahasiswa[i] = arrayMahasiswa; 
        i++; 
    } 
    modelMHS = new DefaultTableModel(objekmahasiswa, namakolom); 
    TABEL.setModel(modelMHS); 
}

    
